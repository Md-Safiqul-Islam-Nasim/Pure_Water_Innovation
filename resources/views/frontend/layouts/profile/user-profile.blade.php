<script>
$(document).ready(function() {
    // Trigger file input click when profile picture is clicked
    $('#profile-picture').off('click').on('click', function() {
        $('#profile_picture_input').click();
    });

    // Handle file input change safely (prevent accidental rebinding)
    $('#profile_picture_input').off('change').on('change', function() {
        const formData = new FormData();
        if ($(this).val() === '') return; // Ensure that file selection is valid

        formData.append('photo', $(this)[0].files[0]);
        formData.append('_token', '{{ csrf_token() }}');

        console.log('FormData created with file: ', $(this)[0].files[0]); // Debugging log

        $.ajax({
            url: "{{ route('update.user.profile.picture') }}",
            type: 'POST',
            data: formData,
            processData: false,
            contentType: false,
            success: function(data) {
                console.log('AJAX success response: ', data);
                if (data.success) {
                    $('#profile-picture').attr('src', data.image_url);
                    toastr.success('Profile picture updated successfully.');
                } else {
                    toastr.error(data.message);
                }
            },
            error: function(e) {
                console.error('AJAX error response: ', e);

                if (e.status === 422) {
                    toastr.error(e.responseJSON?.message);
                } else {
                    toastr.error('Unexpected error occurred.');
                }
            }
        });
    });
});
</script>

